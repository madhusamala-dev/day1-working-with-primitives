package com.trainingmug.java;

public class CustomerInfo {

    public static void main(String[] args) {
       /*
        working with primitives
        */
        byte age;
        age = 35;
        System.out.println("Age : " + age);

        short salary;
        salary = 3453;
        System.out.println("Order Value : " + salary);

        int annualSalary;
        annualSalary = 534534534;
        System.out.println("Annual Salary : " + annualSalary);

        long totalSavings;
        totalSavings = 3453535345345L;
        System.out.println("Total Savings : " + totalSavings);

        float avgSalary;
        avgSalary = 23423.23f;
        System.out.println("Average Salary : " + avgSalary);

        double averageAnnualRevenue;
        averageAnnualRevenue = 23424242.234;
        System.out.println("Average Annual Revenue : " +
                averageAnnualRevenue);

        char gender;
        gender = 'M';
        System.out.println("Gender : " + gender);

        boolean isEligible;
        isEligible = true;
        System.out.println("Eligible : " + isEligible);

    }
}
